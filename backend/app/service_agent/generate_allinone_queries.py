def save_queries(queries, filename="allinone_test_queries.json"):
    """쿼리를 JSON 파일로 저장"""
    output_path = Path(__file__).parent / filename

    # 통계 정보
    stats = {
        "total_queries": len(queries),
        "by_category": {},
        "by_intent": {}
    }

    for query in queries:
        category = query["category"]
        intent = query["expected_intent"]

        stats["by_category"][category] = stats["by_category"].get(category, 0) + 1
        stats["by_intent"][intent] = stats["by_intent"].get(intent, 0) + 1

    output = {
        "metadata": {
            "description": "All-in-One Agent System Test Queries",
            "total_queries": len(queries),
            "statistics": stats,
            "categories": list(stats["by_category"].keys())
        },
        "queries": queries
    }

    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(output, f, ensure_ascii=False, indent=2)

    print(f"\n[OK] {len(queries)} queries generated and saved to {output_path}")
    print(f"\n[Statistics]")
    for category, count in stats["by_category"].items():
        print(f"  {category}: {count} queries")

    return output_path


